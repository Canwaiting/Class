/*
用给定字符串的第一个字符开始匹配原字符串，若成功，
则继续匹配下一个字符，若失败则让给定字符串的第一
个字符串重新开始匹配原字符串的下一个字符。若完全
匹配则返回原字符串中此次匹配的头位置，否则返回0
*/

int BF(char S[],T[])
{
    int start = 0;
    int i = 0,j = 0;
    while(S[i]!='\0' && T[j]!='\0')
    {
        if(S[i]==T[j])
        {
            i++;
            j++;
        }
        else
        {   
            start++;
            j = 0;
            i = start;
        }
    }
    if(T[j]=='\0')
    {
        return start+1;
    }
    return 0;
}

